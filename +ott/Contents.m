% OTT Optical Tweezers Toolbox
%
% The optical tweezers toolbox can be used to simulate optical scattering,
% and dynamics of particles in optical traps.  The toolbox is split into
% the following sub-packages.
%
% To use a class from any of these packages, simply prefix the class
% name with the packages, for example
%
%   beam = ott.beam.paraxial.Gaussian()
%
% Sub-packages
%   +beam       - Descriptions of beams
%   +scat       - Methods to calculate optical scattering
%   +shapes     - Descriptions of different shaped objects
%   +drag       - Drag tensor calculation methods
%   +dynamics   - Methods for simulating particle dynamics
%   +tools      - Useful tools for calculating various physical properties
%   +utils      - Utility functions (mostly used by other toolbox functions)
%   +ui         - Graphical user interfaces
%
