% ott.beam.abstract Abstract representations of beams
%
% This sub-package contains abstract representations of beams.
% These beams beams have all the properties of the beam they represent
% but don't implement methods to calculate the fields.
%
% Abstract beams inherit from the beam.Properties class but not the
% beam.Beam class.
%
% Base class
%   Abstract          -- Base class for abstract beam representations.
%
% Beam representations
%   Gaussian          -- Gaussian beam.
%   HermiteGaussian   -- Hermite-Gaussian beam.
%   LaguerreGaussian  -- Laguerre-Gaussian beam.
%   PlaneWave         -- Plane wave beam.
%
